int	ft_atoi(const char *str)
{
	int toi ;
	int c;
	int si;

	c = 0;
	toi = 0;
	si = 1;
	while ((str[c] == ' ') ||(8 < str[c] && str[c] < 14))
	   c++;
	if(str[c] == '-')
	{
		si =si - 2;
		c++;
	}
	while (isdigit(str[c]) == 1)
		{
			toi = toi * 10 +str[c] - 48;
			c++;
		}
	return (toi * si);
}
